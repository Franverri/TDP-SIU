    private boolean isEmailValid(String email) {
        //TODO: Replace this with your own logic
        boolean login = false;
        if(email.contains("alumno")){
            login = true;
            esAlumno = true;
        } else if (email.contains("docente")){
            login = true;
            esAlumno = false;
        }
        return login;
    }

